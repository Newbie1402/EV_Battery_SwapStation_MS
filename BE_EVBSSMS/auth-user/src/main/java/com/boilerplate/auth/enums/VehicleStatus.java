package com.boilerplate.auth.enums;

/**
 * Enum định nghĩa trạng thái phương tiện của tài xế
 */
public enum VehicleStatus {
    /**
     * Phương tiện đang hoạt động
     */
    ACTIVE,

    /**
     * Phương tiện đang bảo dưỡng
     */
    MAINTENANCE,

    /**
     * Phương tiện không hoạt động
     */
    INACTIVE
}

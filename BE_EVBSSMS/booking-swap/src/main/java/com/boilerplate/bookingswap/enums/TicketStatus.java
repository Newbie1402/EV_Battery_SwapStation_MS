package com.boilerplate.bookingswap.enums;

/**
 * Enum trạng thái của Ticket:
 * - OPEN: Mở
 * - IN_PROGRESS: Đang xử lý
 * - RESOLVED: Đã giải quyết
 * - CLOSED: Đóng
 */
public enum TicketStatus {
    OPEN,   // Mở
    IN_PROGRESS, // Đang xử lý
    RESOLVED, // Đã giải quyết
    CLOSED // Đóng
}
